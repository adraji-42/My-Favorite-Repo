	/* |    |0x123|0x124|0x125|0x126|0x127|     |     |    | */
	/* o---------------------------------------------------o */
	// use malloc function:
	char s[] = "adam";
	// malloc(number byte)
	// malloc reserves a place in memory
	char *t = malloc(strlen(s) + 1);
	// if you not have space in memory malloc return [NULL]
	if (t == NULL)
		return ;
	strcpy(t, s);
	t[0] = 'A';
	printf("%s\n", s); // print adam
	printf("%s", t);   // print Adam
	// Free:
	// free(return malloc function : 0xA23)
	// free up reserved memory space
	free(t);
