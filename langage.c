void pointers(void)
{
	/* +---------------+         +----------------+   */
	/* |   Variable    |         |    Pointer     |   */
	/* |      n        |         |       p1       |   */
	/* +---------------+         +----------------+   */
	/* |   Value: 50   |         |  Address of n  |   */
	/* | Address: 0xA2 | <-----+ |   Value: 0xA2  |   */
	/* +---------------+         +----------------+   */

	int n = 50;
	// Declare a pointer to an int and assign it the address of n
	int *p1 = &n;
	int *p2 = &n;
//____________________________________________________________________

	// Print the address of n in two ways - both should give the same result
	printf("%p", &n);
	printf("%p", p1);
//____________________________________________________________________

	// Print the value of n in three different ways - all should print 50
	printf("%d", n);
	printf("%d", *p1);
	printf("%d", *&n);
//____________________________________________________________________

	// A pointer is a variable that stores the address of another variable
	// The '&' operator gives the address of a variable
	// The '*' operator (dereferencing) gets the value stored at a memory address
	// '%p' is used to print pointer (address) values, cast to (void*) for portability
	// pointer give hexadecimal number
//____________________________________________________________________
}
